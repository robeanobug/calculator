function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

console.log(add(1, 2)); // expect 3
console.log(subtract(2, 1)); // expect 1
console.log(multiply(2, 4)); // expect 8
console.log(divide(4, 2)); // expect 2